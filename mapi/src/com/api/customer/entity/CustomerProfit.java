package com.api.customer.entity;

public class CustomerProfit{
	private Float customer_sales = 0f;
	private Float customer_profit = 0f;
	
	public Float getCustomer_sales() {
		return customer_sales;
	}
	public void setCustomer_sales(Float customer_sales) {
		this.customer_sales = customer_sales;
	}
	public Float getCustomer_profit() {
		return customer_profit;
	}
	public void setCustomer_profit(Float customer_profit) {
		this.customer_profit = customer_profit;
	}
}